package q06;

public class MyTest {
	public static void main(String[] args) {
		int sum = 0;
		
		for(int i = 1 ; i <= 100 ; i++) {	//이 줄은 수정하지 마시오.
			//여기에 코드를 작성하시오.
			
			sum += i;	//이 줄을 포함해서 아래 코드는 수정하지 마시오.
		}
		
		System.out.println(sum);
	}
}
